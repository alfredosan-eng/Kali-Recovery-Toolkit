###############################################################################
# Header
###############################################################################

inspector_header() {

    local TITLE="$1"

    echo
    echo "=============================================================="
    printf "%30s\n" "$TITLE"
    echo "=============================================================="
    echo

}

###############################################################################
# Row
###############################################################################

inspector_row() {

    local LABEL="$1"
    local VALUE="$2"
    local STATUS="$3"

    printf "%-22s %-30s %-10s\n" \
        "$LABEL" \
        "$VALUE" \
        "$STATUS"

}

###############################################################################
# Section
###############################################################################

inspector_section() {

    local TITLE="$1"

    echo
    echo "--------------------------------------------------------------"
    echo "$TITLE"
    echo

}

###############################################################################
# Footer
###############################################################################

inspector_footer() {

    echo
    echo "=============================================================="

}

###############################################################################
#
# Inspector Section
#
###############################################################################

inspector_section() {

    echo

    echo "$1"

    echo "--------------------------------------------------------------"

}
