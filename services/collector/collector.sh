#!/usr/bin/env bash

###############################################################################
# Kali Recovery Toolkit
# Collector Service
###############################################################################

###############################################################################
# User Information
###############################################################################

get_current_user() {
    whoami
}

###############################################################################
# Host Information
###############################################################################

get_hostname() {
    hostname
}

###############################################################################
# Kernel Information
###############################################################################

get_kernel() {
    uname -r
}

###############################################################################
# Architecture Information
###############################################################################

get_architecture() {
    uname -m
}

###############################################################################
# Execution Time
###############################################################################

get_execution_time() {
    date
}

###############################################################################
# Distribution Information
###############################################################################

collect_distribution() {

    if [[ -f /etc/os-release ]]; then
        grep "^PRETTY_NAME=" /etc/os-release | cut -d= -f2 | tr -d '"'
    else
        echo "Unknown"
    fi

}

###############################################################################
# Uptime
###############################################################################

collect_uptime() {

    uptime -p

}

###############################################################################
# Memory
###############################################################################

collect_memory_total() {

    free -h | awk '/^Mem:/ {print $2}'

}

###############################################################################
# Disk Usage
###############################################################################

collect_disk_usage() {

    df -h / | awk 'NR==2 {print $5}'

}

###############################################################################
# Shell
###############################################################################

collect_shell() {

    echo "$SHELL"

}

###############################################################################
# Snapshot Collectors
###############################################################################

collect_snapshot_kernel() {

    uname -r

}

collect_snapshot_hostname() {

    hostname

}

collect_snapshot_distribution() {

    collect_distribution

}

collect_snapshot_architecture() {

    uname -m

}

collect_snapshot_memory() {

    free -h

}

collect_snapshot_disk() {

    df -h

}

collect_snapshot_uptime() {

    uptime

}

collect_snapshot_shell() {

    echo "$SHELL"

}

###############################################################################
# Future Snapshot Collectors
###############################################################################

collect_snapshot_network() {

    ip addr 2>/dev/null

}

collect_snapshot_packages() {

    if command -v dpkg >/dev/null 2>&1; then
        dpkg -l
    fi

}

collect_snapshot_mounts() {

    mount

}

collect_snapshot_filesystems() {

    lsblk -f

}
