#!/usr/bin/env bash

###############################################################################
# Kali Recovery Toolkit
# Report Service
###############################################################################

REPORT_FILE=""

###############################################################################
# Initialize Report
###############################################################################

report_init() {

    local report_name="$1"

    # Si el nombre contiene una ruta, utilizarla directamente.
    if [[ "$report_name" == */* ]]; then

        REPORT_FILE="$report_name"

        mkdir -p "$(dirname "$REPORT_FILE")"

    else

        mkdir -p reports

        REPORT_FILE="reports/${report_name}"

    fi

    : > "$REPORT_FILE"

}

###############################################################################
# Report Header
###############################################################################

report_header() {

    local title="$1"

    cat << EOF >> "$REPORT_FILE"
======================================================
Kali Recovery Toolkit
${title}
======================================================

Generated:
$(date)

EOF

}

###############################################################################
# Report Section
###############################################################################

report_section() {

    local key="$1"
    local value="$2"

    printf "%-22s %s\n" "${key}:" "${value}" >> "$REPORT_FILE"

}

###############################################################################
# Report Message
###############################################################################

report_message() {

    echo "$1" >> "$REPORT_FILE"

}

###############################################################################
# Report Footer
###############################################################################

report_footer() {

cat << EOF >> "$REPORT_FILE"

======================================================

End of Report

======================================================
EOF

}

###############################################################################
# Save Report
###############################################################################

report_save() {

    return 0

}
