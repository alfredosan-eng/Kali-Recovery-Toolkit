#!/usr/bin/env bash

###############################################################################
# KRT Sprint Installer
# SP-008 - Recovery Engine Foundation
###############################################################################

set -e

echo
echo "=============================================================="
echo "        Kali Recovery Toolkit - Sprint Installer"
echo "=============================================================="
echo
echo "Installing SP-008 Recovery Engine..."
echo

###############################################################################
# Directory Structure
###############################################################################

mkdir -p modules/recovery/tests

###############################################################################
# module.conf
###############################################################################

cat > modules/recovery/module.conf <<'EOF'
###############################################################################
# Recovery Module Configuration
###############################################################################

MODULE_NAME="Recovery"

MODULE_VERSION="1.0"

MODULE_AUTHOR="Alfredo San"

MODULE_ENABLED="true"
EOF

###############################################################################
# README
###############################################################################

cat > modules/recovery/README.md <<'EOF'
# Recovery Module

## Purpose

The Recovery Module compares the current system against the latest snapshot
generated by the Backup Module.

Current implementation compares:

- Kernel
- Hostname
- Distribution
- Architecture

Future versions will compare:

- Memory
- Disk
- Installed Packages
- EFI
- GRUB
- Filesystems

The Recovery Module never modifies the system.

It only analyzes differences.
EOF

###############################################################################
# module.sh
###############################################################################

cat > modules/recovery/module.sh <<'EOF'
#!/usr/bin/env bash

###############################################################################
# Recovery Module
###############################################################################

source "${ROOT_DIR}/services/collector/collector.sh"
source "${ROOT_DIR}/services/health/health.sh"
source "${ROOT_DIR}/services/report/report.sh"

###############################################################################
# Public API
###############################################################################

run_recovery() {

    log_info "Initializing Recovery Module..."

    _find_latest_snapshot

    _load_snapshot

    _collect_current_state

    _compare_system

    _print_report

    _generate_report

    log_success "Recovery analysis completed."

}

###############################################################################
# Snapshot Discovery
###############################################################################

_find_latest_snapshot() {

    SNAPSHOT_DIR="$(find backups -mindepth 1 -maxdepth 1 -type d | sort | tail -n1)"

    if [[ -z "$SNAPSHOT_DIR" ]]; then

        log_error "No snapshots found."

        exit 1

    fi

}

###############################################################################
# Snapshot Loading
###############################################################################

_load_snapshot() {

    SNAPSHOT_KERNEL="$(cat "${SNAPSHOT_DIR}/metadata/kernel.txt")"

    SNAPSHOT_HOSTNAME="$(cat "${SNAPSHOT_DIR}/metadata/hostname.txt")"

    SNAPSHOT_DISTRIBUTION="$(cat "${SNAPSHOT_DIR}/metadata/distribution.txt")"

    SNAPSHOT_ARCHITECTURE="$(cat "${SNAPSHOT_DIR}/metadata/architecture.txt")"

}

###############################################################################
# Current State
###############################################################################

_collect_current_state() {

    CURRENT_KERNEL="$(collect_snapshot_kernel)"

    CURRENT_HOSTNAME="$(collect_snapshot_hostname)"

    CURRENT_DISTRIBUTION="$(collect_snapshot_distribution)"

    CURRENT_ARCHITECTURE="$(collect_snapshot_architecture)"

}
###############################################################################
# Comparison Engine
###############################################################################

_compare_system() {

    KERNEL_STATUS="$(_compare_value \
        "$SNAPSHOT_KERNEL" \
        "$CURRENT_KERNEL")"

    HOSTNAME_STATUS="$(_compare_value \
        "$SNAPSHOT_HOSTNAME" \
        "$CURRENT_HOSTNAME")"

    DISTRIBUTION_STATUS="$(_compare_value \
        "$SNAPSHOT_DISTRIBUTION" \
        "$CURRENT_DISTRIBUTION")"

    ARCHITECTURE_STATUS="$(_compare_value \
        "$SNAPSHOT_ARCHITECTURE" \
        "$CURRENT_ARCHITECTURE")"

}

###############################################################################
# Generic Comparator
###############################################################################

_compare_value() {

    local snapshot="$1"
    local current="$2"

    if [[ "$snapshot" == "$current" ]]; then

        echo "PASS"

    else

        echo "ERROR"

    fi

}

###############################################################################
# Terminal Report
###############################################################################

_print_report() {

    echo
    echo "=============================================================="
    echo "                     KRT Recovery"
    echo "=============================================================="
    echo

    printf "%-18s %-15s\n" "Kernel" "$KERNEL_STATUS"

    printf "%-18s %-15s\n" "Hostname" "$HOSTNAME_STATUS"

    printf "%-18s %-15s\n" "Distribution" "$DISTRIBUTION_STATUS"

    printf "%-18s %-15s\n" "Architecture" "$ARCHITECTURE_STATUS"

    echo
    echo "=============================================================="

}

###############################################################################
# Recovery Report
###############################################################################

_generate_report() {

    report_init "recovery_report.txt"

    report_header "Recovery Report"

    report_section "Snapshot" "$SNAPSHOT_DIR"

    report_section "Kernel" "$KERNEL_STATUS"

    report_section "Hostname" "$HOSTNAME_STATUS"

    report_section "Distribution" "$DISTRIBUTION_STATUS"

    report_section "Architecture" "$ARCHITECTURE_STATUS"

    report_footer

    report_save

}

EOF

###############################################################################
# Permissions
###############################################################################

chmod +x modules/recovery/module.sh
###############################################################################
# Dispatcher Integration
###############################################################################

echo
echo "Updating CLI Dispatcher..."

if ! grep -q -- '--recover' core/cli/dispatch.sh; then

cat >> core/cli/dispatch.sh <<'EOF'

###############################################################################
# Recovery
###############################################################################

elif [[ "$1" == "--recover" ]]; then

    source "${ROOT_DIR}/modules/recovery/module.sh"

    run_recovery

EOF

fi

###############################################################################
# Framework API Integration
###############################################################################

if [[ -f core/framework/framework_api.sh ]]; then

cat >> core/framework/framework_api.sh <<'EOF'

###############################################################################
# Recovery API
###############################################################################

framework_run_recovery() {

    source "${ROOT_DIR}/modules/recovery/module.sh"

    run_recovery

}

EOF

fi

###############################################################################
# Test Runner Update
###############################################################################

cat > tests/run_tests.sh <<'EOF'
#!/usr/bin/env bash

echo
echo "========================================"
echo "        KRT Framework Test Runner"
echo "========================================"
echo

PASS_COUNT=0
FAIL_COUNT=0

run_test() {

    local name="$1"
    local file="$2"

    if [[ -f "$file" ]]; then

        echo "[PASS] $name"

        ((PASS_COUNT++))

    else

        echo "[FAIL] $name"

        ((FAIL_COUNT++))

    fi

}

echo "Running framework tests..."
echo

run_test "Collector Service" "services/collector/collector.sh"

run_test "Logger Service" "services/logger/logger.sh"

run_test "Report Service" "services/report/report.sh"

run_test "Health Service" "services/health/health.sh"

run_test "Backup Module" "modules/backup/module.sh"

run_test "Doctor Module" "modules/doctor/module.sh"

run_test "Recovery Module" "modules/recovery/module.sh"

echo
echo "----------------------------------------"
echo "PASS : $PASS_COUNT"

echo "FAIL : $FAIL_COUNT"
echo "----------------------------------------"

if [[ "$FAIL_COUNT" -eq 0 ]]; then

    echo
    echo "Framework validation completed successfully."

else

    echo
    echo "Framework validation failed."

fi

EOF

chmod +x tests/run_tests.sh
###############################################################################
# Installation Summary
###############################################################################

echo
echo "=============================================================="
echo "Recovery Engine Installation Summary"
echo "=============================================================="

check_file() {

    local file="$1"

    if [[ -f "$file" ]]; then
        printf "[ OK ] %s\n" "$file"
    else
        printf "[FAIL] %s\n" "$file"
    fi

}

check_file modules/recovery/module.sh
check_file modules/recovery/module.conf
check_file modules/recovery/README.md
check_file tests/run_tests.sh

echo
echo "Recovery Engine successfully installed."

echo
echo "New command available:"
echo
echo "    ./krt --recover"
echo

echo "Run the following commands:"
echo
echo "    ./krt --recover"
echo "    ./tests/run_tests.sh"
echo

echo "=============================================================="
echo "SP-008 Installation Completed"
echo "=============================================================="
